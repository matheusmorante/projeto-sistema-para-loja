    private void submitCustomerFormBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitCustomerFormBtnActionPerformed
        DefaultTableModel customersTableModel = (DefaultTableModel) customersTable.getModel();
        String name = customerNameField.getText();
        String cpf = customerCpfField.getText();
        String cellphone = customerCellphoneField.getText();
        String tellphone = customerTellphoneField.getText();
        String logradouro = customerLogradouroField.getText();
        String addressNumber = customerAddressNumberField.getText();
        String complement = customerComplementField.getText();
        String bairro = customerBairroField.getText();
        String city = customerCityField.getText();
        String uf = customerUfField.getText();
        String completeAddress = logradouro + ", " + addressNumber + ", " +  complement +
                ", " +  bairro + ", " +  city + ", " +  uf;
        
        customersTableModel.addRow(new Object[] {
            name,
            cpf,
            cellphone,
            tellphone,
            completeAddress
        });
    }//GEN-LAST:event_submitCustomerFormBtnActionPerformed

